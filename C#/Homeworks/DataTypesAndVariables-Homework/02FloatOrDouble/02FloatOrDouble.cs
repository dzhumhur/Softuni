using System;

class FloatOrDouble
{
    static void Main()
    {
        double firstNum = 34.567839023;
        float secondNum = 12.345f;
        double thirdNum = 8923.1234857;
        float fourthNum = 3456.091f;

        Console.WriteLine(firstNum + " - double");
        Console.WriteLine(secondNum + " - float");
        Console.WriteLine(thirdNum + " - double");
        Console.WriteLine(fourthNum + " - float");
    }
}